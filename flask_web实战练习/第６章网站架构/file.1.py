# 反向代理和正向代理
# 正向代理,作为一个媒介将互联网上获取的资源返回给相关联的客户端.代理和客户段在一个局域网,对于服务端是透明的.反向代理,根据客户端的请求,从后端的服务端上获取资源
# 然后再将这些资源返回给客户端.代理和服务器在一个局域网,对客户端是透明的.
