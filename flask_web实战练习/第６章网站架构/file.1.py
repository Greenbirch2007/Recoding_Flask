# web服务器与应用服务器的区别
#１． web服务器负责处理http协议，应用服务器既可以处理http内容，也能处理其他协议，比如RPC
# 2. web服务器用于处理静态页面的内容,对于脚本语言(如python)产生的动态内容,它通过WSGI接口交给应用服务器来处理
# 3. 一般应用服务器都继承了web服务器,自带的应用服务器甚至可以支持应用级别的功能,比如连接池,事务支持,消息服务等.虽然集成了web服务器,主要是为了调试方便,处于
# 处于性能好稳定性考虑,应用服务器并不能在生产环境中使用
